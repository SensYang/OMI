public class ChatMoreBoard {
    private int imgRes;
    private int textRes;

    public ChatMoreBoard(int imgRes, int textRes) {
        this.imgRes = imgRes;
        this.textRes = textRes;
    }

    public int getImgRes() {
        return imgRes;
    }

    public int getTextRes() {
        return textRes;
    }

}
